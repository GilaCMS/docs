<span id="about"></span><h1>About<a class="headerlink" href="#about" title="Permalink to this headline">¶</a></h1>
<p>Gila CMS is an open-source and free content management system built with php7. Built with MVC architecture, is very easy to develop on it any customized solution. It is licensed under BSD 3-Clause License.
The website is <a class="reference external" href="http://gilacms.com">gilacms.com</a></p>
<div class="section" id="why-choose-gila">
<span id="why-choose-gila"></span><h2>Why choose Gila?<a class="headerlink" href="#why-choose-gila" title="Permalink to this headline">¶</a></h2>
<p>Gila CMS is a good option for self-hosted blogs or startup websites</p>
<ul class="simple">
<li>Installs by default a blogging system with social integrations.</li>
<li>No coding skills are required to install or maintain the website</li>
<li>Themes that includes are responsive, that makes it accessible in all devices.</li>
<li>It is fast, and compresses the content where it needs to.</li>
</ul>
</div>
